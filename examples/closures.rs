fn main()
{
	let a = |x: i32| -> i32 {
		x * 100
	};
	println!("a(8) = {}", a(8));

	// closure可以省略类型声明，使用Rust自动类型判断机制
	let a = |x| {
		x * 100
	};
	println!("a(8) = {}", a(8));
}
